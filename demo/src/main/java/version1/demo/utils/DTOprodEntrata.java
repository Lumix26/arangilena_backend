package version1.demo.utils;

public class DTOprodEntrata {
    
    private String nome;
    private String descrz;
    private double prezzo;
    private String nomeCategoria;
    private int scorte;

    public DTOprodEntrata() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getDescrz() {
        return descrz;
    }

    public void setDescrz(String descrz) {
        this.descrz = descrz;
    }

    public double getPrezzo() {
        return prezzo;
    }

    public void setPrezzo(double prezzo) {
        this.prezzo = prezzo;
    }

    public String getNomeCategoria() {
        return nomeCategoria;
    }

    public void setNomeCategoria(String nomeCategoria) {
        this.nomeCategoria = nomeCategoria;
    }

    public int getScorte() {
        return scorte;
    }

    public void setScorte(int scorte) {
        this.scorte = scorte;
    }

    
}
